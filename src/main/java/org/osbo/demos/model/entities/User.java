public class User {

    private int user;
    private int idTienda;

    /**
     * @return the user
     */
    public int getUser() {
        return user;
    }

    /**
     * @param user the user to set
     */
    public void setUser(int user) {
        this.user = user;
    }

    /**
     * @return the idTienda
     */
    public int getIdTienda() {
        return idTienda;
    }

    /**
     * @param idTienda the idTienda to set
     */
    public void setIdTienda(int idTienda) {
        this.idTienda = idTienda;
    }
}
